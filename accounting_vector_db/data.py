def get_category_summary() -> dict:
    """返回各类科目的数量统计"""
    from collections import Counter
    cats = Counter(s["category"] for s in ACCOUNTING_SUBJECTS)
    return dict(cats)


def search_by_code(code: str) -> list:
    """按科目编号搜索"""
    return [s for s in ACCOUNTING_SUBJECTS if s["code"] == code]


def search_by_name(name: str) -> list:
    """按科目名称模糊搜索"""
    return [s for s in ACCOUNTING_SUBJECTS if name in s["name"]]


def search_by_report_item(report_item: str) -> list:
    """按对应报表项目搜索"""
    return [s for s in ACCOUNTING_SUBJECTS if report_item in s["report_item"]]
